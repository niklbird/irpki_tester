pub struct CertificatePaths {
    pub ski: Vec<&'static str>,
}

impl CertificatePaths {
    pub fn init_nested() -> CertificatePaths {
        let prefix = [CONTENTINFO, CONTENT, SIGNEDDATA, CERTIFICATEIMP].to_vec();
        let mut cert_paths = Self::init_cert();
        cert_paths.ski = prefix.iter().chain(cert_paths.ski.iter()).cloned().collect();
        cert_paths
    }

    pub fn init_cert() -> CertificatePaths {
        CertificatePaths {
            ski: [CERTIFICATECHOICES, CERTIFICATE, EXTENSIONSFIELD, EXTENSIONS, SUBJECTKEYIDENTIFIEREXT, SUBJECTKEYIDENTIFIEROCTETSTRING, SUBJECTKEYIDENTIFIER].to_vec(),
        }
    }
}

